/* -----------------------------------------------------------
   Language switching
   ----------------------------------------------------------- */

const SUPPORTED = ["az", "tr", "ru", "en"];
const DEFAULT_LANG = "az";
const HTML_LANG_MAP = { az: "az", tr: "tr", ru: "ru", en: "en" };

function applyLang(lang) {
  if (!SUPPORTED.includes(lang)) lang = DEFAULT_LANG;
  const dict = i18n[lang];

  document.documentElement.lang = HTML_LANG_MAP[lang];

  // text content
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    const attr = el.getAttribute("data-i18n-attr");
    const value = dict[key];
    if (value === undefined) return;
    if (attr) {
      el.setAttribute(attr, value);
    } else {
      el.textContent = value;
    }
  });

  // active button state
  document.querySelectorAll(".lang-switcher button").forEach(b => {
    b.classList.toggle("active", b.dataset.lang === lang);
  });

  try { localStorage.setItem("lang", lang); } catch (e) {}
}

function detectInitialLang() {
  // AZ is always the default. Browser language is intentionally NOT auto-detected
  // (most Azerbaijani users have a TR or RU browser locale, which would mislead).
  // Only respect an explicit user choice that was saved in localStorage.
  try {
    const saved = localStorage.getItem("lang");
    if (saved && SUPPORTED.includes(saved)) return saved;
  } catch (e) {}
  return DEFAULT_LANG;
}

/* -----------------------------------------------------------
   Copy buttons
   ----------------------------------------------------------- */

function setupCopyButtons() {
  document.querySelectorAll(".copy").forEach(btn => {
    btn.addEventListener("click", async () => {
      const num = btn.getAttribute("data-number") || "";
      const ok = await copyToClipboard(num);
      if (!ok) return;

      const lang = document.documentElement.lang || DEFAULT_LANG;
      const dict = i18n[lang] || i18n[DEFAULT_LANG];
      const span = btn.querySelector("[data-i18n]");
      if (!span) return;

      const original = dict.copy;
      span.textContent = dict.copied;
      btn.classList.add("copied");

      clearTimeout(btn._t);
      btn._t = setTimeout(() => {
        span.textContent = original;
        btn.classList.remove("copied");
      }, 1800);
    });
  });
}

async function copyToClipboard(text) {
  try {
    if (navigator.clipboard && window.isSecureContext) {
      await navigator.clipboard.writeText(text);
      return true;
    }
  } catch (e) { /* fall through */ }

  // fallback for non-secure contexts / older browsers
  try {
    const ta = document.createElement("textarea");
    ta.value = text;
    ta.setAttribute("readonly", "");
    ta.style.position = "fixed";
    ta.style.top = "-1000px";
    document.body.appendChild(ta);
    ta.select();
    const ok = document.execCommand("copy");
    document.body.removeChild(ta);
    return ok;
  } catch (e) {
    return false;
  }
}

/* -----------------------------------------------------------
   Lightbox — photos + documents
   Back button closes the lightbox first, not the whole site.
   ----------------------------------------------------------- */

function setupLightbox() {
  // Inject minimal styles so no style.css change is needed.
  const styleEl = document.createElement("style");
  styleEl.textContent = `
    body.lightbox-open { overflow: hidden; }
    .gallery-item img, .doc-item img { cursor: zoom-in; }
    .lightbox-overlay {
      position: fixed; inset: 0;
      background: rgba(0, 0, 0, 0.92);
      z-index: 1000;
      display: flex;
      align-items: center;
      justify-content: center;
      padding: 24px;
      cursor: zoom-out;
      opacity: 0;
      transition: opacity 0.18s ease-out;
    }
    .lightbox-overlay.open { opacity: 1; }
    .lightbox-overlay img {
      max-width: 100%;
      max-height: 100%;
      object-fit: contain;
      box-shadow: 0 8px 32px rgba(0, 0, 0, 0.5);
      border-radius: 6px;
      cursor: default;
    }
    .lightbox-close {
      position: absolute;
      top: 16px; right: 16px;
      width: 44px; height: 44px;
      border-radius: 50%;
      background: rgba(255, 255, 255, 0.14);
      color: #fff;
      border: 1px solid rgba(255, 255, 255, 0.25);
      font-size: 24px;
      line-height: 1;
      cursor: pointer;
      display: flex;
      align-items: center;
      justify-content: center;
      padding: 0;
      font-family: inherit;
    }
    .lightbox-close:hover { background: rgba(255, 255, 255, 0.24); }
  `;
  document.head.appendChild(styleEl);

  let overlay = null;
  let isOpen = false;

  function openLightbox(src, alt) {
    if (isOpen) return;
    isOpen = true;

    overlay = document.createElement("div");
    overlay.className = "lightbox-overlay";
    overlay.setAttribute("role", "dialog");
    overlay.setAttribute("aria-modal", "true");

    const img = document.createElement("img");
    img.src = src;
    img.alt = alt || "";

    const closeBtn = document.createElement("button");
    closeBtn.className = "lightbox-close";
    closeBtn.type = "button";
    closeBtn.setAttribute("aria-label", "Close");
    closeBtn.innerHTML = "&times;";

    overlay.appendChild(img);
    overlay.appendChild(closeBtn);
    document.body.appendChild(overlay);
    document.body.classList.add("lightbox-open");

    // Trigger fade-in on next frame.
    requestAnimationFrame(() => overlay && overlay.classList.add("open"));

    closeBtn.addEventListener("click", (e) => {
      e.stopPropagation();
      closeLightbox(true);
    });
    overlay.addEventListener("click", (e) => {
      if (e.target === overlay) closeLightbox(true);
    });

    // Push exactly one history entry per open. The popstate listener
    // closes the lightbox when that entry is popped (Back button).
    try { history.pushState({ lightbox: true }, ""); } catch (e) {}
  }

  function closeLightbox(consumeHistory) {
    if (!isOpen) return;
    isOpen = false;

    if (overlay && overlay.parentNode) {
      overlay.parentNode.removeChild(overlay);
    }
    overlay = null;
    document.body.classList.remove("lightbox-open");

    // If we still own the pushed entry, walk back so history stays clean.
    // (When close was triggered BY popstate, we must NOT call history.back again.)
    if (consumeHistory) {
      try {
        if (history.state && history.state.lightbox) {
          history.back();
        }
      } catch (e) {}
    }
  }

  // Click delegation — works for both .gallery-item img (photos)
  // and .doc-item img (documents). On <a> wrappers we prevent the
  // default new-tab navigation that was causing the original mobile bug.
  document.querySelectorAll(".gallery-item img, .doc-item img").forEach(img => {
    img.addEventListener("click", (e) => {
      const link = img.closest("a");
      if (link) e.preventDefault();
      openLightbox(img.currentSrc || img.src, img.alt);
    });
  });

  // Back button / back gesture.
  window.addEventListener("popstate", () => {
    if (isOpen) closeLightbox(false);
  });

  // Escape closes on desktop.
  document.addEventListener("keydown", (e) => {
    if (e.key === "Escape" && isOpen) closeLightbox(true);
  });
}

/* -----------------------------------------------------------
   Sticky donate CTA
   Visible from the Story section onward, hidden in hero and
   hidden again once Donation/Footer come into view.
   ----------------------------------------------------------- */

function setupDonateCta() {
  const cta = document.querySelector(".cta-donate");
  const story = document.querySelector(".story");
  const donation = document.querySelector(".donation");
  const footer = document.querySelector(".footer");
  if (!cta || !story || !donation) return;

  let pastStoryStart = false;
  let donationInView = false;
  let footerInView = false;

  function update() {
    const show = pastStoryStart && !donationInView && !footerInView;
    cta.classList.toggle("visible", show);
  }

  // Primary observer — on the Story section. CTA appears when the story
  // intersects the viewport or has already been scrolled past.
  const storyObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      pastStoryStart = entry.isIntersecting || entry.boundingClientRect.top < 0;
      update();
    });
  }, { threshold: 0 });
  storyObserver.observe(story);

  // Secondary observer — hide when Donation or Footer is in view.
  const hideObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.target === donation) donationInView = entry.isIntersecting;
      else if (entry.target === footer) footerInView = entry.isIntersecting;
    });
    update();
  }, { threshold: 0.1 });
  hideObserver.observe(donation);
  if (footer) hideObserver.observe(footer);

  // Smooth scroll to donation on tap — center it vertically in the viewport.
  cta.addEventListener("click", () => {
    donation.scrollIntoView({ behavior: "smooth", block: "center" });
  });
}

/* -----------------------------------------------------------
   Init
   ----------------------------------------------------------- */

document.addEventListener("DOMContentLoaded", () => {
  applyLang(detectInitialLang());

  document.querySelectorAll(".lang-switcher button").forEach(b => {
    b.addEventListener("click", () => applyLang(b.dataset.lang));
  });

  setupCopyButtons();
  setupLightbox();
  setupDonateCta();
});
